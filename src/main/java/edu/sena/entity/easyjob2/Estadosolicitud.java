package edu.sena.entity.easyjob2;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

/**
 *
 * @author fabio
 */
@Entity
@Table(name = "tbl_estadosolicitud")
@NamedQueries({
    @NamedQuery(name = "Estadosolicitud.findAll", query = "SELECT e FROM Estadosolicitud e")})
public class Estadosolicitud implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 30)
    @Column(name = "Descripcion")
    private String descripcion;
    @Basic(optional = false)
    @NotNull
    @Lob
    @Size(min = 1, max = 65535)
    @Column(name = "Observaciones")
    private String observaciones;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "solicitudId", fetch = FetchType.LAZY)
    private Collection<Aprobacion> aprobacionCollection;

    public Estadosolicitud() {
    }

    public Estadosolicitud(Integer id) {
        this.id = id;
    }

    public Estadosolicitud(Integer id, String descripcion, String observaciones) {
        this.id = id;
        this.descripcion = descripcion;
        this.observaciones = observaciones;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getObservaciones() {
        return observaciones;
    }

    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }

    public Collection<Aprobacion> getAprobacionCollection() {
        return aprobacionCollection;
    }

    public void setAprobacionCollection(Collection<Aprobacion> aprobacionCollection) {
        this.aprobacionCollection = aprobacionCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Estadosolicitud)) {
            return false;
        }
        Estadosolicitud other = (Estadosolicitud) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "edu.sena.entity.easyjob2.Estadosolicitud[ id=" + id + " ]";
    }
    
}
